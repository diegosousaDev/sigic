    private void listarFuncionarios() {

        List<Funcionario> lista = funcionarioDao.findAll();
        DefaultTableModel dados = (DefaultTableModel) tabelaConsultaFuncionario.getModel();
        dados.setNumRows(0);

        lista.stream().forEach((c) -> {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getEmail(),
                c.getCpf(),
                c.getNascimento(),
                c.getData_admissao(),
                c.getData_saida(),
                c.getFuncao(),
                c.getCarteira(),
                c.getStatus()
            });
        });
    }

